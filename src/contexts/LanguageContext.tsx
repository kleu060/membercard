export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>('zh-TW');
  const [isInitialized, setIsInitialized] = useState(false);

  useEffect(() => {
    // Only run this effect on the client side
    if (typeof window !== 'undefined') {
      const savedLanguage = localStorage.getItem('language') as Language;
      if (savedLanguage && translations[savedLanguage]) {
        setLanguage(savedLanguage);
      } else {
        // Try to detect browser language as fallback
        const browserLang = navigator.language;
        if (browserLang.startsWith('zh')) {
          // For Chinese variants, check if it's Traditional or Simplified
          if (browserLang === 'zh-TW' || browserLang === 'zh-HK' || browserLang === 'zh-MO') {
            setLanguage('zh-TW');
          } else {
            setLanguage('zh-CN');
          }
        } else if (browserLang.startsWith('en')) {
          setLanguage('en');
        }
        // Default to zh-TW if no match
      }
      setIsInitialized(true);
    }
  }, []);

  const handleSetLanguage = (lang: Language) => {
    setLanguage(lang);
    if (typeof window !== 'undefined') {
      localStorage.setItem('language', lang);
    }
  };

  const t = (key: string): string => {
    return translations[language][key as keyof typeof translations['zh-TW']] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage: handleSetLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}